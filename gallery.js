let currentIndex = 0;

function changePhoto(direction) {
    currentIndex += direction;  
    
    if (currentIndex >= photoTripLog.length) currentIndex = 0;
    if (currentIndex < 0) currentIndex = photoTripLog.length - 1;
    
    const photoElement = document.getElementById("active-screen-photo");
    const metadataElement = document.getElementById("photo-metadata");
    
    photoElement.style.opacity = 0.3;
    
    setTimeout(() => {
        photoElement.src = photoTripLog[currentIndex].src;
        
        metadataElement.innerText = photoTripLog[currentIndex].metadata;
        
        photoElement.style.opacity = 1;
    }, 120);
}